//Valores maior e menor

//Maior
console.log(`Valor maior: ${Math.max(1, 2, 4, 8, 16, 32, 64, 128)}`);

//Menor
console.log(`Valor menor: ${Math.min(1, 2, 4, 8, 16, 32, 64, 128)}`);


//Sorteador
console.log(`Valor aleatorio entre 0 e 1: ${Math.random()}`);


